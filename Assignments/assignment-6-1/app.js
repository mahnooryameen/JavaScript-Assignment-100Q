var number= +prompt("enter a number");
document.write("the value of a is:" + number + "<hr/>");


document.write("the value of ++a is:" + ++number + "<br>");
document.write("now the value of a is:" + number + "<hr/>");



document.write("the value of a++ is:" + number++ + "<br>");
document.write("now the value of a is:" + number + "<hr/>");


document.write("the value of --a is:" + --number + "<br>");
document.write("now the value of a is:" + number + "<hr/>");

document.write("the value of a-- is:" + number-- + "<br>");
document.write("now the value of a is:" + number + "<hr/>");